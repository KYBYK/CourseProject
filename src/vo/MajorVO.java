package vo;

public class MajorVO {
	private String m_idx, m_code, m_name, m_yn, m_need_point;

	public String getM_idx() {
		return m_idx;
	}

	public void setM_idx(String m_idx) {
		this.m_idx = m_idx;
	}

	public String getM_code() {
		return m_code;
	}

	public void setM_code(String m_code) {
		this.m_code = m_code;
	}

	public String getM_name() {
		return m_name;
	}

	public void setM_name(String m_name) {
		this.m_name = m_name;
	}

	public String getM_yn() {
		return m_yn;
	}

	public void setM_yn(String m_yn) {
		this.m_yn = m_yn;
	}

	public String getM_need_point() {
		return m_need_point;
	}

	public void setM_need_point(String m_need_point) {
		this.m_need_point = m_need_point;
	}
}
